#!/bin/bash
  
  ### ====================================================================================================
  ### [START]
  ### ====================================================================================================
    
    echo
    echo "[INSTALL]"
    
    
    
  ### ====================================================================================================

  install_src_path="${BASH_SOURCE[0]%/*}/src/install.sh"
  [ ! -e "${install_src_path}" ] && echo "Missing ${install_src_path}!" && exit 1
  source "${install_src_path}" || exit 1
  
  
  
  ### ====================================================================================================
  ### [Config Files]
  ### ====================================================================================================
    
    copy_file ".gitignore"
    copy_file ".gitconfig" "GIT_USERNAME,GIT_EMAIL"
